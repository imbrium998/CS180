
public class RectangleTest {

	public static void main(String[] args) 
	{
		
		double width1 = 1;
		double length1 = 1;
		
		double width2 = 10;
		double length2 = 12;
		
		double area1 = width1 * length1;
		double area2 = width2 * length2;
		
		double perim1 = 2.0 * (width1 + length1);
		double perim2 = 2.0 * (width2 + length2);
		
		System.out.println(area1);
		System.out.println(area2);
		
		System.out.println(perim1);
		System.out.println(perim2);
		

	}

}
